#include "Student.hpp"

/*******************************************************************************
 * Constructor
*******************************************************************************/
Student::Student() {
    name = new string;
    age  = new int;

    cout << "Student object created!" << endl;
}

/*******************************************************************************
 * Destructor
*******************************************************************************/
Student::~Student() {
    delete name;
    delete age;

    cout << "Student object destroyed!" << endl;
}

/*******************************************************************************
 * Mutators
*******************************************************************************/
void Student::setName(string s) {
    *name = s;
}

void Student::setAge(int i) {
    *age = i;
}

/*******************************************************************************
 * Accessors
*******************************************************************************/
string Student::getName() const {
    return *name;
}

int Student::getAge() const {
    return *age;
}
